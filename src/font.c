long
font_size(const char* str)
{
    assert(str != NULL);
    long size = 0;

    char c;
    while ((c = *str++) != '\0') {
        c = c - '0';
        assert(c >= 0 && c <= 9);

        unsigned short glyph = font[(unsigned char)c];
        for (long i = 0; i < 4*4; i++) {
            char quad = (glyph >> i) & 1;
            if (quad) {
                size += FLOATS_PER_QUAD * sizeof(float);
            }
        }
    }

    return size;
}

long
font_vertices(const char* str)
{
    assert(str != NULL);
    long vertices = 0;

    char c;
    while ((c = *str++) != '\0') {
        c = c - '0';
        assert(c >= 0 && c <= 9);

        unsigned short glyph = font[(unsigned char)c];
        for (long i = 0; i < 4*4; i++) {
            char quad = (glyph >> i) & 1;
            if (!quad) continue;

            vertices += VERTICES_PER_QUAD;
        }
    }

    return vertices;
}

void
font_print(const char* str, float* buffer, long size)
{
    assert(str != NULL);
    assert(buffer != NULL);
    assert(size >= font_size(str));

    long x = 0;
    long y = 0;

    char c;
    while ((c = *str++) != '\0') {
        c = c - '0';
        assert(c >= 0 && c <= 9);

        unsigned short glyph = font[(unsigned char)c];
        for (long i = 0; i < 4*4; i++) {
            char quad = (glyph >> i) & 1;
            if (!quad) continue;

            for (long v = 0; v < VERTICES_PER_QUAD; v++) {
                *buffer++ = quads[i][v*2 + 0] + x;
                *buffer++ = quads[i][v*2 + 1] + y;
            }
        }

        x += 1;
    }
}
